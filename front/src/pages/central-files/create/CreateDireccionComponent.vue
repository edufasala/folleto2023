<template>
  <div class="row q-pl-xs">
    <div class="col-12 row items-center">
      <div class="text-bold">DIRECCIONES</div>
      <q-space />
      <q-btn :loading="loading" round dense flat icon="add_circle_outline" color="blue" @click="direccionDialogClick">
        <q-tooltip>Crear Direccion</q-tooltip>
      </q-btn>
    </div>
    <div class="col-12">
      <q-card v-for="d in direccion" :key="d.id" class="bg-grey-1 q-mb-xs">
        <q-card-section class="q-pa-xs">
          <div class="row text-caption">
            <div class="col-12 row items-center">
              Nombre: <b>{{d.direccion}}</b>
              <q-space />
            </div>
            <div class="col-12">
              C. Postal: <b>{{d.codigoPostal}}</b>
            </div>
            <div class="col-12 row items-center">
              Localidad: <b>{{d.localidad}}</b>
            </div>
            <div class="col-12">
              <template v-if="d.phone_direccions.length>0">
                <div v-for="phone in d.phone_direccions" :key="phone.id" class="row">
                  Telefono: <b>{{phone.phone}}</b>
                  <q-space />
                </div>
              </template>
              <template v-else>
              </template>
            </div>
          </div>
        </q-card-section>
      </q-card>
    </div>
    <q-dialog v-model="direccionDialog">
      <q-card class="q-pa-xs" style="width: 35vw !important">
        <q-card-section class="row">
          <div class="text-h6">
            {{direccionOption === 'create' ? 'Crear' : 'Editar'}} Direcci&oacute;n
          </div>
          <q-space />
          <q-btn flat dense icon="cancel" v-close-popup />
        </q-card-section>
        <q-separator />
        <q-form @submit="direccionSubmit">
          <q-card-section class="q-pt-sm">
            <div class="row">
              <div class="col-12">
                <q-input dense outlined v-model="direccionDato.direccion" label="Dirección *"
                         :rules="[val => !!val || 'La dirección es requerida']"/>
              </div>
            </div>
            <div class="row">
              <div class="col-12">
                <q-input dense outlined v-model="direccionDato.codigoPostal" label="Código Postal"/>
              </div>
            </div>
            <div class="row q-pt-md">
              <div class="col-12">
                <q-input dense outlined v-model="direccionDato.localidad" label="Localidad"/>
              </div>
            </div>
            <div class="row q-pt-md">
              <div class="col-12">
                <q-input dense outlined v-model="direccionDato.phone" label="Teléfono" mask="##########"/>
              </div>
            </div>
          </q-card-section>
          <q-separator />
          <q-card-actions align="right">
            <q-btn dense no-caps label="Cancelar" v-close-popup color="red" :loading="loading"/>
            <q-btn dense no-caps :loading="loading" type="submit"
            :label="direccionOption === 'create' ? 'Crear' : 'Editar'"
            :color="direccionOption === 'create' ? 'blue' : 'orange'" />
          </q-card-actions>
        </q-form>
      </q-card>
    </q-dialog>
  </div>
</template>
<script>
export default {
  name: 'CreateDirecionComponent',
  props: {
    empresa: {
      type: Object,
      default: () => {}
    },
    direccion: {
      type: Array,
      default: () => []
    }
  },
  data () {
    return {
      loading: false,
      direccionDialog: false,
      direccionOption: '',
      direccionDato: {}
    }
  },
  methods: {
    deleteDirecion (id) {
      this.$q.dialog({
        title: 'Eliminar',
        message: '¿Desea eliminar la direcciona?',
        cancel: true,
        persistent: true
      }).onOk(() => {
        this.loading = true
        this.$axios.delete('direccion/' + id)
          .then(response => {
            this.$emit('empresaSearch', this.empresa)
          }).catch(error => {
            this.$alert.error(error)
          }).finally(() => {
            this.loading = false
          })
      })
    },
    direccionSubmit () {
      this.$emit('newDireccion', this.direccionDato)
      this.direccionDialog = false
    },
    direccionDialogClick () {
      this.direccionDialog = true
      this.direccionOption = 'create'
      this.direccionDato = {}
    },
    deletePhone (id) {
      this.$q.dialog({
        title: 'Eliminar',
        message: '¿Desea eliminar el telefono?',
        cancel: true,
        persistent: true
      }).onOk(() => {
        this.loading = true
        this.$axios.delete('phoneDireccions/' + id)
          .then(response => {
            this.$emit('empresaSearch', this.empresa)
          }).catch(error => {
            this.$alert.error(error)
          }).finally(() => {
            this.loading = false
          })
      })
    },
    addPhone (direccionId) {
      this.$q.dialog({
        title: 'Agregar Telefono',
        message: 'Ingrese el telefono',
        prompt: {
          model: '',
          type: 'text'
        },
        cancel: true,
        persistent: true
      }).onOk(data => {
        this.loading = true
        this.$axios.post('phoneDireccions', {
          phone: data,
          direccion_id: direccionId
        }).then(response => {
          this.$emit('empresaSearch', this.empresa)
        }).catch(error => {
          this.$alert.error(error)
        }).finally(() => {
          this.loading = false
        })
      })
    },
    addEmail (direccionId) {
      this.$q.dialog({
        title: 'Agregar Email',
        message: 'Ingrese el email',
        prompt: {
          model: '',
          type: 'text'
        },
        cancel: true,
        persistent: true
      }).onOk(data => {
        this.loading = true
        this.$axios.post('emails', {
          email: data,
          direccion_id: direccionId
        }).then(response => {
          this.$emit('empresaSearch', this.empresa)
        }).catch(error => {
          this.$alert.error(error)
        }).finally(() => {
          this.loading = false
        })
      })
    },
    deleteEmail (id) {
      this.$q.dialog({
        title: 'Eliminar',
        message: '¿Desea eliminar el email?',
        cancel: true,
        persistent: true
      }).onOk(() => {
        this.loading = true
        this.$axios.delete('emails/' + id)
          .then(response => {
            this.$emit('empresaSearch', this.empresa)
          }).catch(error => {
            this.$alert.error(error)
          }).finally(() => {
            this.loading = false
          })
      })
    },
    updateEmailStatus (email) {
      this.$axios.put('emails/' + email.id, {
        status: email.status
      })
    }
  }
}
</script>
